    private string ReplaceWords(string message)
    {
        if (string.IsNullOrEmpty(message))
            return message;

        return Regex.Replace(message, "\\b(\\w+)\\b", match =>
        {
            if (SlangReplace.TryGetValue(match.Value.ToLower(), out var replacement))
                return replacement;
            return match.Value;
        });
    }
